import java.util.*;

public class Demo2 {
	
	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		System.out.println();
		int x = sc.nextInt();
		System.out.println(x);
		
		
		float f = sc.nextFloat();
		System.out.println(f);
		
		String str = sc.nextLine();
		System.out.println(str);
		
		
		sc.close();
		
		
	}
}
